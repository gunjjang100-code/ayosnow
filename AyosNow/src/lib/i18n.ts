export function isLocale(value: string | undefined): value is Locale {
  return value === "fil" || value === "en";
}

export function getNavigationItems(locale: Locale): NavigationItem[] {
  if (locale === "en") {
    return [
      { label: "Home", href: "/" },
      { label: "Categories", href: "/categories" },
      { label: "Services", href: "/services" },
      { label: "Quote Request", href: "/quote-request" },
      { label: "Quotes", href: "/quotes" },
      { label: "Chat", href: "/chat" },
      { label: "Bookings", href: "/bookings" },
      { label: "Dashboard", href: "/dashboard" },
    ];
  }

  if (locale === "fil") {
    return [
      { label: "Simula", href: "/" },
      { label: "Mga Kategorya", href: "/categories" },
      { label: "Mga Serbisyo", href: "/services" },
      { label: "Humiling ng Quote", href: "/quote-request" },
      { label: "Mga Quote", href: "/quotes" },
      { label: "Usapan", href: "/chat" },
      { label: "Mga Booking", href: "/bookings" },
      { label: "Buod", href: "/dashboard" },
    ];
  }

  return getNavigationItems(defaultLocale);
}

